
public class Student {

    private String Name;
    private String ID;
    private float Grade;

    public Student() {

        this.Name = Name;
        this.ID = ID;
        this.Grade = Grade;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public float getGrade() {
        return Grade;
    }

    public void setGrade(float grade) {
        Grade = grade;
    }

    public String getID() {
        return ID;
    }

    public void setID(String iD) {
        ID = iD;
    }

}